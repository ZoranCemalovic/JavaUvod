public class TipoviPromenljivih {
    public static void main(String[] args) {

        String a; //Niz karaktera (Ime, prezime, adresa,..)
        int x; //Ceo broj (1,2,3,4,..)
        double y; //Realni broj tj. decimalni broj, koristi se tacka, ne zarez
        float z; //Realni broj sa manjom memorijom
        float u = 1.2351f; //Na kraju broja obavezno je dodati slovo "f" ili "F"
        long k; //Ceo broj sa vecom memorijom (1902979715000, 1234567890,..)
        long j = 11857815L; //Na kraju broja obavezno je dodati slovo "L"
        char c; //Jedan karakter, koristi se apostrof tj, jednostruki navodnik
        boolean b; //Logi;ki tip podataka, moze biti "true" ili "false"

        a="Ime";
        x=1;
        y=5.76;
        z = 1.625f;
        k = 78162571L;
        c = 'u';
        b = true;

        System.out.println(x);



    }
}
